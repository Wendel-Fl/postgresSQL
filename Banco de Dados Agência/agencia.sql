DROP TABLE empresa, filial, funcionario, secretario, motorista, motorista, carro, dirige, dependente, cliente, cliente_locacao, aluga, valor_locacao;

CREATE TABLE empresa (
emp_codigo INT NOT NULL PRIMARY KEY,
emp_nome VARCHAR(30),
emp_endereco VARCHAR(100),
emp_telefone VARCHAR(15) );

CREATE TABLE filial (
emp_codigo INT NOT NULL,
filial_nro INT NOT NULL,
filial_local VARCHAR(30),
filial_data_abertura DATE,
PRIMARY KEY (emp_codigo, filial_nro),
FOREIGN KEY (emp_codigo) REFERENCES empresa(emp_codigo) ON DELETE CASCADE);

CREATE TABLE funcionario (
fun_codigo INT NOT NULL PRIMARY KEY,
primeiro_nome VARCHAR(30),
sobrenome VARCHAR(30),
fun_endereco VARCHAR(100),
fun_sexo CHAR(1),
fun_niver DATE,
fun_salario FLOAT,
tipo_funcionario VARCHAR(10) NOT NULL,
emp_codigo INT REFERENCES empresa(emp_codigo) ON DELETE SET NULL,
cod_supervisor INT,
FOREIGN KEY (cod_supervisor) REFERENCES funcionario(fun_codigo) ON DELETE SET NULL,
CHECK (fun_sexo IN ('m', 'f')),
CHECK (tipo_funcionario IN ('secretario', 'motorista')));


CREATE TABLE secretario (
fun_codigo INT NOT NULL PRIMARY KEY,
num_certificado INT,
data_expedicao DATE,
FOREIGN KEY (fun_codigo) REFERENCES funcionario(fun_codigo) ON DELETE CASCADE);

CREATE TABLE motorista (
fun_codigo INT NOT NULL PRIMARY KEY,
num_carteira INT,
classe CHAR(1),
FOREIGN KEY (fun_codigo) REFERENCES funcionario(fun_codigo) ON DELETE CASCADE);

CREATE TABLE carro (
car_chassi VARCHAR(30) NOT NULL PRIMARY KEY,
car_modelo VARCHAR(30),
car_ano INT,
emp_codigo INT REFERENCES empresa(emp_codigo) ON DELETE SET NULL);

CREATE TABLE dirige (
car_chassi VARCHAR(30) NOT NULL,
fun_codigo INT NOT NULL,
data DATE NOT NULL,
nro_horas INT,
PRIMARY KEY (car_chassi, fun_codigo, data),
FOREIGN KEY (car_chassi) REFERENCES carro(car_chassi)
 ON DELETE CASCADE,
FOREIGN KEY (fun_codigo) REFERENCES motorista(fun_codigo)
 ON DELETE CASCADE);

CREATE TABLE dependente (
fun_codigo INT NOT NULL,
dep_nome VARCHAR(50) NOT NULL,
dep_sexo VARCHAR(10),
dep_data_niver DATE,
parentesco VARCHAR(20),
PRIMARY KEY (fun_codigo, dep_nome),
CHECK (dep_sexo IN ('m', 'f')),
FOREIGN KEY (fun_codigo) REFERENCES funcionario(fun_codigo)
 ON DELETE CASCADE);

CREATE TABLE cliente (
cli_codigo INT NOT NULL PRIMARY KEY,
cli_nome VARCHAR(30),
cli_telefone VARCHAR(15),
emp_codigo INT REFERENCES empresa(emp_codigo) ON DELETE SET NULL);

CREATE TABLE cliente_locacao(
cli_codigo INT NOT NULL REFERENCES cliente(cli_codigo) ON DELETE CASCADE,
data DATE  NOT NULL,
NF_numero int,
NF_valor FLOAT,
desconto FLOAT,
PRIMARY KEY (cli_codigo, data ) );

CREATE TABLE aluga (
cli_codigo INT NOT NULL REFERENCES cliente(cli_codigo) ON DELETE CASCADE,
car_chassi VARCHAR(30) NOT NULL  REFERENCES carro(car_chassi) ON DELETE CASCADE,
PRIMARY KEY (cli_codigo, car_chassi) );

CREATE TABLE valor_locacao(
data_reajuste DATE  NOT NULL,
car_ano int NOT NULL,
car_modelo VARCHAR(30) NOT NULL,
valor FLOAT,
PRIMARY KEY (data_reajuste, car_ano, car_modelo));

